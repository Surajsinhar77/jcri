// Getter method of Date

var nowDate = new Date();
console.log(nowDate.toLocaleDateString());
console.log(nowDate.getDate());             // Date
console.log(nowDate.getFullYear());         // Year    
console.log(nowDate.getMonth());            // Month count 0 to 11
console.log(nowDate.getDay());          //Week days from 1=monday to 7=sunday


